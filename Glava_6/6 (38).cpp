#include <iostream>
#include <algorithm>

int main() {
    int a, b; // Размеры стола (a > b)
    int c, d; // Размеры прямоугольников (c > d)

    std::cout << "Введите размеры стола (a > b):\n";
    std::cout << "a = ";
    std::cin >> a;
    std::cout << "b = ";
    std::cin >> b;

    std::cout << "\nВведите размеры прямоугольников (c > d):\n";
    std::cout << "c = ";
    std::cin >> c;
    std::cout << "d = ";
    std::cin >> d;

    // Проверка корректности ввода
    if (a <= b) {
        std::cout << "Ошибка: a должно быть больше b" << std::endl;
        return 1;
    }
    if (c <= d) {
        std::cout << "Ошибка: c должно быть больше d" << std::endl;
        return 1;
    }

    // Размещение вдоль длинной стороны стола
    int count1 = (a / c) * (b / d);

    // Размещение вдоль короткой стороны стола  
    int count2 = (a / d) * (b / c);

    std::cout << "\nРезультаты:\n";
    std::cout << "При размещении длинной стороной вдоль длинной стороны стола: "
        << count1 << " прямоугольников\n";
    std::cout << "При размещении длинной стороной вдоль короткой стороны стола: "
        << count2 << " прямоугольников\n\n";

    if (count1 > count2) {
        std::cout << "Выгоднее размещать длинной стороной вдоль длинной стороны стола" << std::endl;
    }
    else if (count2 > count1) {
        std::cout << "Выгоднее размещать длинной стороной вдоль короткой стороны стола" << std::endl;
    }
    else {
        std::cout << "Количество прямоугольников одинаково в обоих случаях" << std::endl;
    }

    return 0;
}